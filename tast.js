#!/usr/bin/env -S D:\software\nodejs\node.exe

/**
 * Git pre-commit hook: 在提交代码之前执行。
 * 此脚本会弹出一个系统通知，提示用户正在进行代码提交。
 *
 * 系统环境：
 * - Node.js 可用：是
 * - Node.js 路径：D:\software\nodejs\node.exe
 *
 * 依赖：
 * - 'node-notifier' 模块用于发送系统通知。
 *   如果未安装，请运行 'npm install node-notifier'。
 */

const notifier = require('node-notifier');
const path = require('path');
const os = require('os');
// 确保 notifier 模块已安装
require.resolve('node-notifier');


notifier.notify(
    {
        title: 'Git 提交通知',
        message: '您正在提交代码！请确保您的更改已准备好。',
        icon: path.join(__dirname, 'git-icon.png'), // 可以替换为自定义图标路径
        sound: true, // 播放提示音
        wait: true // 不等待用户点击通知
    },
    function (err, response) {
        if (err) {
            console.error('发送通知时发生错误:', err);
        }
        process.exit(0);
    }
);

